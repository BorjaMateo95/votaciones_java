package Modelos;

/**
 *
 * @author BORJA
 */
public class Partido {
    
    private int id;
    private String nombre;
    private String logo;
    private int votos;

    public Partido(int id, String nombre, String logo, int votos) {
        this.id = id;
        this.nombre = nombre;
        this.logo = logo;
        this.votos = votos;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getLogo() {
        return logo;
    }

    public void setLogo(String logo) {
        this.logo = logo;
    }

    public int getVotos() {
        return votos;
    }

    public void setVotos(int votos) {
        this.votos = votos;
    }
    
    
    
}
